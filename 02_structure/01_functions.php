<?php
/*
 * РІВЕНЬ 2, УРОК 1: Функції (Functions)
 * 
 * Функція - це блок коду, який можна викликати багато разів.
 * Це допомагає не дублювати код (принцип DRY - Don't Repeat Yourself).
 */

// --- 1. ПРОСТА ФУНКЦІЯ ---
function sayHello()
{
    echo "Привіт, світ!<br>";
}

// Виклик функції:
sayHello();
sayHello();

// --- 2. ФУНКЦІЯ З АРГУМЕНТАМИ ---
// Ми можемо передавати дані всередину функції.

function greetUser($name)
{
    echo "Привіт, <strong>$name</strong>!<br>";
}

greetUser("Олексій");
greetUser("Марія");

// --- 3. ПОВЕРНЕННЯ ЗНАЧЕННЯ (RETURN) ---
// Більшість функцій не просто "друкують", а ПОВЕРТАЮТЬ результат, 
// щоб ми могли його використати далі.

function add($a, $b)
{
    $result = $a + $b;
    return $result;
}

$sum = add(10, 20); // $sum стане 30
echo "Сума 10 + 20 = $sum <br>";

// --- 4. ЗНАЧЕННЯ ЗА ЗАМОВЧУВАННЯМ ---
// Якщо аргумент не передали, візьметься дефолтне значення.

function makeCoffee($type = "Капучіно")
{
    return "Ваш $type готовий.<br>";
}

echo makeCoffee(); // "Ваш Капучіно готовий."
echo makeCoffee("Еспресо"); // "Ваш Еспресо готовий."

// --- 5. ТИПІЗАЦІЯ (Суворий режим для якості коду) ---
// Ми можемо вказувати, які типи даних ми чекаємо і які повертаємо.

function multiply(int $x, int $y): int
{
    return $x * $y;
}

// multiply(5, "abc"); // Це викликало б помилку!
echo "5 * 5 = " . multiply(5, 5);

